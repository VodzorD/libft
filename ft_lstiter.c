#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*ls;

	if (lst && f)
	{
		ls = lst;
		while (ls)
		{
			f(ls);
			ls = ls->next;
		}
	}
}
