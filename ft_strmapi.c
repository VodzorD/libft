#include "libft.h"
#include <stdlib.h>

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	index;
	char			*str;

	index = 0;
	if (!s)
		return (NULL);
	str = (char *)malloc(sizeof(char) * ft_strlen(s) + 1);
	if (str)
	{
		while (s[index] != '\0')
		{
			str[index] = f(index, s[index]);
			index++;
		}
		str[index] = '\0';
		return (str);
	}
	return (0);
}
